#!/bin/bash

# fpga on
adb shell "fpga_on.sh"
sleep 1

# set manual mode
adb shell "echo 1 > /sys/class/light_ccb/common/manual_control"
sleep 1

# cam open
adb shell "echo 4 0x0000 0x01 0x00 0x00 0x02 > /sys/class/light_ccb/i2c_interface/i2c_w"
sleep 40

# Set offsets
adb shell "echo 25 0x004E 0x00 0x01 0x16 0xF1 0x00 0x01 0x00 0x54 0x9C 0x01 0x00 0x54 0x9C 0x5B 0x00 0xAB 0x38 0x2E 0x00 0x7F 0x6A 0x5B 0x00 0xAB 0x38   > /sys/class/light_ccb/i2c_interface/i2c_w"
sleep 1

# Configure nth frame to be captured
adb shell "echo 7 0x004E 0x00 0x01 0x04 0xF3 0x00 0x02 0x00 > /sys/class/light_ccb/i2c_interface/i2c_w"
sleep 1





