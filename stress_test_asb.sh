#!/bin/bash
 while true
 do
	echo "Open camera"
	adb shell "echo 6 0x0000 0x00 0x00 0x02 0x00 0x00 0x02> /sys/class/i2c-adapter/i2c-11/i2c-0/0-0010/i2c_w"
	sleep 1
	echo "Set UCID preview"
	adb shell "echo 4 0x0000 0x00 0x10 0x03 0x00 > /sys/class/i2c-adapter/i2c-11/i2c-0/0-0010/i2c_w"
	sleep 1	
	echo "Set sensitive 0x3000"
	adb shell "echo 11 0x0000 0x30 0x00 0x02 0x00 0x00 0x03 0x00 0x00 0x30 0x00 0x00 > /sys/class/i2c-adapter/i2c-11/i2c-0/0-0010/i2c_w"
	sleep 1
	echo "Set exposure 10000000"
	adb shell "echo 15 0x0000 0x32 0x00 0x02 0x00 0x00 0x03 0x00 0x40 0x42 0x0f 0x00 0x00 0x00 0x00 0x00 > /sys/class/i2c-adapter/i2c-11/i2c-0/0-0010/i2c_w"
	sleep 1
	echo "Stream on A1"
	adb shell "echo 8 0x0000 0x02 0x00 0x02 0x00 0x00 0x11 0x00 0x00 > /sys/class/i2c-adapter/i2c-11/i2c-0/0-0010/i2c_w"
	sleep 10
	echo "Stream off A1"
	adb shell "echo 8 0x0000 0x02 0x00 0x02 0x00 0x00 0x10 0x00 0x00 > /sys/class/i2c-adapter/i2c-11/i2c-0/0-0010/i2c_w"
	echo "Close camera"
	adb shell "echo 6 0x0000 0x00 0x00 0x01 0x00 0x00 0x00 > /sys/class/i2c-adapter/i2c-11/i2c-0/0-0010/i2c_w"
done